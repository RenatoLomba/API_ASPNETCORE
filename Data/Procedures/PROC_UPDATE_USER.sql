CREATE DEFINER=`root`@`localhost` PROCEDURE `PROC_UPDATE_USER`(
	IN PROC_ID CHAR(36),
    IN PROC_UPDATE_AT DATETIME(6),
    IN PROC_USER_NAME VARCHAR(60),
    IN PROC_EMAIL VARCHAR(100)
)
BEGIN

UPDATE user
SET 
`Name`=PROC_USER_NAME, 
`Email`=PROC_EMAIL,
`UpdateAt`=PROC_UPDATE_AT
WHERE `Id`=PROC_ID;

END