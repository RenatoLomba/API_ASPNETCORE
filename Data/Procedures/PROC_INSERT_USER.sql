CREATE DEFINER=`root`@`localhost` PROCEDURE `PROC_INSERT_USER`(
	IN PROC_ID CHAR(36),
    IN PROC_CREATE_AT DATETIME(6),
    IN PROC_USER_NAME VARCHAR(60),
    IN PROC_EMAIL VARCHAR(100)
)
BEGIN

INSERT INTO user (`Id`, `CreateAt`, `Name`, `Email`)
VALUES (PROC_ID, PROC_CREATE_AT, PROC_USER_NAME, PROC_EMAIL);

END